    /// 某分类下新建空白提示词的默认模板骨架
    static func blankTemplate(for category: PromptCategory) -> String {
        let vars = category.variableChips.joined(separator: "\n")
        return """
        你是一位短剧创作助手。请根据以下信息完成「\(category.displayName)」相关任务。

        \(vars.isEmpty ? "" : "可用输入：\n\(vars)\n")
        要求：
        1. …
        2. …
        3. 只输出结果，不要额外解释
        """
    }
